package SRP.example_001;

public class ComputerActions {

    public void onComputer() {
        System.out.println("Turn on the Computer!!");
    }

    public void offComputer() {
        System.out.println("Turn off the Computer!!");
    }

    public void restartComputer() {
        System.out.println("Restart the Computer");
    }

    public void sleepComputer() {
        System.out.println("Suspend the Computer");
    }
}
